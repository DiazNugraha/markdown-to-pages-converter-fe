<div class="container">
  <h1 id="about-our-app">About This App</h1>

  <p>Welcome to Markdown to Svelte HTML Converter!</p>

  <p>
    This app is designed to streamline the process of converting Markdown files
    into HTML pages specifically for the Svelte framework. With just a few
    simple steps, you can effortlessly transform your Markdown content into
    ready-to-use Svelte pages.
  </p>

  <h2 id="key-features">Key Features</h2>

  <ol>
    <li>
      <strong>Folder and Markdown Conversion</strong>: This app allows you to
      convert entire folders containing Markdown (.md) files into HTML pages
      tailored for Svelte.
    </li>
    <li>
      <strong>Easy-to-Use Interface</strong>: Upload a zip file with your
      folders and Markdown files, and let the app handle the rest.
    </li>
    <li>
      <strong>Automated Processing</strong>: Sit back and relax while the
      backend processes your files and prepares them for use in your Svelte
      project.
    </li>
    <li>
      <strong>Seamless Integration</strong>: Receive a zip file containing the
      converted HTML pages and Svelte files. Simply export the content and
      rename the folder to &ldquo;routes&rdquo; to integrate with your existing
      Svelte app.
    </li>
  </ol>

  <h2 id="how-it-works">How It Works</h2>

  <ol>
    <li>
      <strong>Upload Zip File</strong>: Start by uploading a zip file that
      contains your folders and Markdown files.
    </li>
    <li>
      <strong>Wait for Processing</strong>: This app will process the zip file
      and convert the Markdown files into HTML pages formatted for Svelte.
    </li>
    <li>
      <strong>Download the Result</strong>: Once the processing is complete, you
      will receive a zip file with the converted pages and Svelte files.
    </li>
    <li>
      <strong>Integrate with Your Svelte App</strong>: Extract the files from
      the zip, rename the folder to &ldquo;routes&rdquo;, and overwrite the
      existing &ldquo;routes&rdquo; folder in your Svelte project. Your new
      pages are now ready to use!
    </li>
  </ol>

  <p>
    If you have any questions or need support, please feel free to reach out.
  </p>
